# Create a program that prints out the even numbers in an array

Array_1 = [21, 22, 23, 24, 25, 26]

for num in Array_1:
    if num % 2 == 0:
        print(num, "is an even number")

Array_2 = [112, 13, 27, 17, 43, 69, 0]

for num in Array_2:
    if num % 2 == 0:
        print('\t', num, "is an even number")
